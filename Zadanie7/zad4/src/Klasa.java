public class Klasa {
    private int a;
    private int b;
    private int c;
    private int d;
    private int e;

    public Klasa(int a, int b, int c, int d, int e) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
        this.e = e;
    }
    public String toString(){
        return this.a + " " + this.b + " " + this.c + " " + this.d + " " + this.e;
    }
}
